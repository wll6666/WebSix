


// 定向到student_list.jsp 并显示修改结果
 


		package com.demo.servlet;

		import java.io.IOException;
		import java.sql.Connection;
		import java.sql.DriverManager;
		import java.sql.ResultSet;
		import java.sql.SQLException;
		import java.sql.Statement;
		import java.util.ArrayList;
		import java.util.List;

		import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
		import javax.servlet.http.HttpServletRequest;
		import javax.servlet.http.HttpServletResponse;

        import com.demo.bean.student;
@WebServlet("/FindServlet0")
		public class FindServlet0 extends HttpServlet {
			private static final long serialVersionUID = 1L;

			protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
				try {
					// 加载数据库驱动，注册到驱动管理器
					Class.forName("com.mysql.jdbc.Driver");
					// 数据库连接字符串
					String url = "jdbc:mysql://localhost:3306/mysql1";
					// 数据库用户名
					String username = "root";
					// 数据库密码
					String password = "123";
					// 创建Connection连接
					Connection conn = DriverManager.getConnection(url,username,password);
					// 获取Statement
					Statement stmt = conn.createStatement();
					// 添加student信息的SQL语句
					String sql = "select * from student";
					// 执行查询
					ResultSet rs = stmt.executeQuery(sql);
					// 实例化List对象
					List<student> list = new ArrayList<student>();
					// 判断光标向后移动，并判断是否有效
					while(rs.next()){
						// 实例化student对象
						student student = new student();
						// 对id属性赋值
						student.setId(rs.getString("id"));
						// 对name属性赋值
						student.setName(rs.getString("name"));
						// 对sex属性赋值
						student.setSex(rs.getString("sex"));
						// 对tel属性赋值
						student.setTel(rs.getString("tel"));
						// 对qq属性赋值
						student.setQq(rs.getString("qq"));
						// 对id_card属性赋值
						student.setId_card(rs.getString("id_card"));
						// 将图书对象添加到集合中
						list.add(student);
					}
					// 将成员集合放置到request之中
					request.setAttribute("list", list);
					rs.close();		// 关闭ResultSet
					stmt.close();	// 关闭Statement
					conn.close();	// 关闭Connection
				} catch (ClassNotFoundException e) {
					e.printStackTrace();
				} catch (SQLException e) {
					e.printStackTrace();
				}
				// 请求转发到student_list.jsp
				request.getRequestDispatcher("student_list.jsp").forward(request, response);
			}
		

	

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		doGet(request, response);
	}

}
